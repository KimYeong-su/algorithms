arr = [[0 for _ in range(7)]for _ in range(7)]
base = [1, 2, 1, 3, 2, 4, 2, 5, 4, 6, 5, 6, 6, 7, 3, 7]
for i in range(8):
    u, v = base[i*2]-1, base[i*2+1]-1
    arr[v][u] = 1
    arr[u][v] = 1

for i in range(7):
    print(arr[i])